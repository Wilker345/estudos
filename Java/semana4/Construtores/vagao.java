package semana4.Construtores;

public class vagao {
	private int NumeroDeSerie;
	private char tipo;
	private int CapacidadeDeCarga;
	private int ComprimentoEntreTesteiras;
	private int ComprimentoDosEngates;
	
	public int getNumeroDeSerie() {
		return NumeroDeSerie;
	}
	public void setNumeroDeSerie(int numeroDeSerie) {
		NumeroDeSerie = numeroDeSerie;
	}
	public char getTipo() {
		return tipo;
	}
	public void setTipo(char tipo) {
		this.tipo = tipo;
	}
	public int getCapacidadeDeCarga() {
		return CapacidadeDeCarga;
	}
	public void setCapacidadeDeCarga(int capacidadeDeCarga) {
		CapacidadeDeCarga = capacidadeDeCarga;
	}
	public int getComprimentoEntreTesteiras() {
		return ComprimentoEntreTesteiras;
	}
	public void setComprimentoEntreTesteiras(int comprimentoEntreTesteiras) {
		ComprimentoEntreTesteiras = comprimentoEntreTesteiras;
	}
	public int getComprimentoDosEngates() {
		return ComprimentoDosEngates;
	}
	public void setComprimentoDosEngates(int comprimentoDosEngates) {
		ComprimentoDosEngates = comprimentoDosEngates;
	}
}
