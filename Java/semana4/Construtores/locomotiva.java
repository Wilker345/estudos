package semana4.Construtores;

public class locomotiva {
	private int NumeroDeSerie;
	private int CapacidadeDeTracao;
	private int Comprimento;
	
	public int getNumeroDeSerie() {
		return NumeroDeSerie;
	}
	public void setNumeroDeSerie(int numeroDeSerie) {
		NumeroDeSerie = numeroDeSerie;
	}
	public int getCapacidadeDeTracao() {
		return CapacidadeDeTracao;
	}
	public void setCapacidadeDeTracao(int capacidadeDeTracao) {
		CapacidadeDeTracao = capacidadeDeTracao;
	}
	public int getComprimento() {
		return Comprimento;
	}
	public void setComprimento(int comprimento) {
		Comprimento = comprimento;
	}
}
